#!/usr/bin/env npx tsx
/**
 * context-menu postInstall script
 *
 * Windows only: installs Nilesoft Shell (if needed), creates claude-code.nss
 * with right-click entries for Claude Code (YOLO, YOLO+TUI, Normal).
 *
 * FR-1: Auto-install Nilesoft Shell via winget
 * FR-2: Global path to launch-claude-tui.ps1 via ~/.dev-pomogator/scripts/
 */

import { execSync } from 'node:child_process';
import * as fs from 'node:fs';
import * as os from 'node:os';
import * as path from 'node:path';
import { fileURLToPath } from 'node:url';

const PREFIX = '[context-menu]';
const NILESOFT_DIR = 'C:\\Program Files\\Nilesoft Shell';
const SHELL_NSS = path.join(NILESOFT_DIR, 'shell.nss');
const IMPORTS_DIR = path.join(NILESOFT_DIR, 'imports');
const CLAUDE_NSS = path.join(IMPORTS_DIR, 'claude-code.nss');
const IMPORT_LINE = "import 'imports/claude-code.nss'";

// This module lives at <root>/tools/context-menu/postinstall.ts
const MODULE_DIR = path.dirname(fileURLToPath(import.meta.url));

// Global launch-script location. The generated NSS (see generateNss) points the
// "Claude Code (YOLO + TUI)" entry at this exact path, so the file MUST exist here or
// the right-click launch does nothing (powershell.exe opens, can't find -File, exits).
const GLOBAL_SCRIPTS_DIR = path.join(os.homedir(), '.dev-pomogator', 'scripts');
const GLOBAL_LAUNCH_SCRIPT = path.join(GLOBAL_SCRIPTS_DIR, 'launch-claude-tui.ps1');

function log(message: string): void {
  console.log(`  ${PREFIX} ${message}`);
}

export function isWindows(): boolean {
  return process.platform === 'win32';
}

function isNilesoftInstalled(): boolean {
  return fs.existsSync(SHELL_NSS);
}

function installNilesoft(): boolean {
  log('Installing Nilesoft Shell via winget...');
  try {
    execSync(
      'winget install Nilesoft.NilesoftShell --accept-package-agreements --accept-source-agreements',
      { stdio: 'inherit', timeout: 120000 },
    );
    return fs.existsSync(SHELL_NSS);
  } catch {
    log('⚠ winget install failed. Install manually: winget install Nilesoft.NilesoftShell');
    return false;
  }
}

export function generateNss(): string {
  // Single source of truth: the exact path copyLaunchScript() writes to, so the NSS entry and
  // the installed file can never drift apart. Backslashes for the NSS/Windows side.
  const launchScript = GLOBAL_LAUNCH_SCRIPT.replace(/\//g, '\\');

  return `// Claude Code context menu entries (auto-generated by dev-pomogator)
// Reload: Ctrl+Right-click desktop → Shell → Reload

// Standard (non-elevated) entries
item(type='dir|back' sep='top' title='Claude Code (YOLO + TUI)' image='@app.dir\\imports\\claude-icon.ico' cmd='powershell.exe' args='-ExecutionPolicy Bypass -File "${launchScript}" -Yolo -ProjectDir "@sel.dir"')
item(type='dir|back' where=package.exists("WindowsTerminal") title='Claude Code (YOLO)' image='@app.dir\\imports\\claude-icon.ico' cmd='wt.exe' args='-d "@sel.dir\\." -- cmd /k claude --dangerously-skip-permissions')
item(type='dir|back' where=package.exists("WindowsTerminal") title='Claude Code' image='@app.dir\\imports\\claude-icon.ico' cmd='wt.exe' args='-d "@sel.dir\\." -- cmd /k claude')

// Elevated (admin) submenu — required for Hyper-V cmdlets, ADK installs, system file edits
menu(type='dir|back' sep='bottom' title='Claude Code (Admin)' image='@app.dir\\imports\\claude-icon.ico')
{
    item(admin=true title='Claude Code (YOLO + TUI)' image='@app.dir\\imports\\claude-icon.ico' cmd='powershell.exe' args='-ExecutionPolicy Bypass -File "${launchScript}" -Yolo -ProjectDir "@sel.dir"')
    item(where=package.exists("WindowsTerminal") admin=true title='Claude Code (YOLO)' image='@app.dir\\imports\\claude-icon.ico' cmd='wt.exe' args='-d "@sel.dir\\." -- cmd /k claude --dangerously-skip-permissions')
    item(where=package.exists("WindowsTerminal") admin=true title='Claude Code' image='@app.dir\\imports\\claude-icon.ico' cmd='wt.exe' args='-d "@sel.dir\\." -- cmd /k claude')
}
`;
}

/** Path to the launch script bundled in the repo/plugin tree (<root>/scripts/launch-claude-tui.ps1). */
export function bundledLaunchScriptPath(): string {
  return path.join(MODULE_DIR, '..', '..', 'scripts', 'launch-claude-tui.ps1');
}

/**
 * FR-3: Copy the bundled launch script to the global path referenced by the generated NSS.
 *
 * generateNss() hardcodes ~/.dev-pomogator/scripts/launch-claude-tui.ps1, but nothing else
 * populates that path under the canonical v2 plugin (postinstall is not auto-run anymore).
 * Without this copy the first menu entry ("Claude Code (YOLO + TUI)") launches a powershell
 * window that immediately closes because -File points at a non-existent file.
 *
 * Returns true on success, false if the source is missing (logged, non-fatal). Plain user-dir
 * write — no elevation needed.
 */
export function copyLaunchScript(src?: string, dest?: string): boolean {
  const source = src ?? bundledLaunchScriptPath();
  const target = dest ?? GLOBAL_LAUNCH_SCRIPT;

  if (!fs.existsSync(source)) {
    log(`⚠ Launch script not found at ${source} — context menu (YOLO + TUI) entry will not work`);
    return false;
  }

  try {
    fs.mkdirSync(path.dirname(target), { recursive: true });
    fs.copyFileSync(source, target);
    log(`✓ Launch script copied to ${target}`);
    return true;
  } catch (e) {
    log(`⚠ Could not copy launch script to ${target}: ${(e as Error).message}`);
    return false;
  }
}

function elevatedCopy(src: string, dest: string): boolean {
  try {
    const srcEscaped = src.replace(/\//g, '\\');
    const destEscaped = dest.replace(/\//g, '\\');
    execSync(
      `powershell.exe -NoProfile -Command "Start-Process cmd -ArgumentList '/c copy /Y \\"${srcEscaped}\\" \\"${destEscaped}\\"' -Verb RunAs -Wait"`,
      { stdio: 'inherit', timeout: 30000 },
    );
    return true;
  } catch {
    return false;
  }
}

function ensureImportLine(): void {
  try {
    const content = fs.readFileSync(SHELL_NSS, 'utf-8');
    if (content.includes(IMPORT_LINE)) return;

    const updated = content.trimEnd() + '\n' + IMPORT_LINE + '\n';
    const tempFile = path.join(process.cwd(), 'temp-shell.nss');
    fs.writeFileSync(tempFile, updated, 'utf-8');

    if (elevatedCopy(tempFile, SHELL_NSS)) {
      log('✓ Added import to shell.nss');
    } else {
      log('⚠ Could not update shell.nss — add manually: ' + IMPORT_LINE);
    }

    try { fs.unlinkSync(tempFile); } catch { /* ignore */ }
  } catch {
    log('⚠ Could not read shell.nss');
  }
}

function reloadNilesoft(): void {
  const shellExe = path.join(NILESOFT_DIR, 'shell.exe');

  // Attempt reload — if it kills Explorer, force-restart it
  try {
    execSync(`"${shellExe}" -restart`, { stdio: 'pipe', timeout: 10000 });
    log('✓ Nilesoft Shell reloaded');
  } catch {
    try {
      execSync(
        `powershell.exe -NoProfile -Command "Start-Process '${shellExe}' -ArgumentList '-restart' -Verb RunAs -Wait"`,
        { stdio: 'pipe', timeout: 10000 },
      );
      log('✓ Nilesoft Shell reloaded (elevated)');
    } catch {
      log('⚠ Reload failed — will restart Explorer');
    }
  }

  // Safety net: ensure explorer.exe is running (shell.exe -restart can kill it)
  ensureExplorerRunning();
}

function ensureExplorerRunning(): void {
  try {
    execSync(
      'powershell.exe -NoProfile -Command "if (-not (Get-Process explorer -EA SilentlyContinue)) { Start-Process explorer.exe }"',
      { encoding: 'utf-8', stdio: 'pipe', timeout: 5000 },
    );
  } catch { /* best-effort */ }
}

function main(): void {
  if (!isWindows()) {
    log('Skipped (not Windows)');
    return;
  }

  if (!isNilesoftInstalled()) {
    if (!installNilesoft()) {
      log('⚠ Nilesoft Shell not available — context menu not configured');
      return;
    }
  }

  // Ensure the launch script exists at the global path the NSS references (FR-3).
  copyLaunchScript();

  const nssContent = generateNss();

  let existingContent = '';
  try { existingContent = fs.readFileSync(CLAUDE_NSS, 'utf-8'); } catch { /* file doesn't exist yet */ }

  if (existingContent === nssContent) {
    ensureImportLine();
    log('✓ Context menu already up to date (skipped reload)');
    return;
  }

  // Write temp → elevated copy
  const tempFile = path.join(process.cwd(), 'temp-claude-code.nss');
  fs.writeFileSync(tempFile, nssContent, 'utf-8');

  if (elevatedCopy(tempFile, CLAUDE_NSS)) {
    log('✓ Created claude-code.nss');
  } else {
    log('⚠ Elevated copy failed — run as admin or copy manually');
    log(`  Source: ${tempFile}`);
    log(`  Target: ${CLAUDE_NSS}`);
    return; // don't cleanup temp so user can copy manually
  }

  try { fs.unlinkSync(tempFile); } catch { /* ignore */ }

  ensureImportLine();
  reloadNilesoft();
  log('✓ Context menu configured and reloaded');
}

// Run only when invoked directly (not when imported for testing)
const __filename = fileURLToPath(import.meta.url);
if (process.argv[1] === __filename) {
  main();
}
