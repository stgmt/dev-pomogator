        Open-Worktree -WorktreePath $match.Path
    }
    else {
        Write-Host "Worktree not found: $Open" -ForegroundColor Red
    }
}
elseif ($Remove) {
    Remove-Worktree -Name $Remove
}
else {
    Show-Menu
}
